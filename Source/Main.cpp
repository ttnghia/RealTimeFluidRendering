int main(int argc, char* argv[])
{
    bool            vsync = true;
    AppConfigReader config("config.ini");
    if(config.isFileLoaded() && config.hasParam("vsync")) {
        vsync = (config.getIntValue("vsync") == 1);
    }
    //    __BNNQt_RunMainWindowSplashScreen(MainWindow, argc, argv, vsync, QDir::currentPath() + "/Textures/FlashScreen/");
    __BNNQt_RunMainWindow(MainWindow, argc, argv, vsync);
}